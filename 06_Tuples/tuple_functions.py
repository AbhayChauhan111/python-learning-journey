tuple1=(2,4,5,6,89,12,56,33,78,'Mangoes','Bananas','Grapes')
#accessing element of tuple using index
print(tuple1[2])
print(tuple1[-1])  # Accessing last element
print(tuple1[1:4])  # Accessing a range of elements
print(len(tuple1))  # Getting the length of the tuple
print(tuple1.count(6))  # Counting occurrences of an element
print(tuple1.index('Bananas'))  # Finding the index of an element
